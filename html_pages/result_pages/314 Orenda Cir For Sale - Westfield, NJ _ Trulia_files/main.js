			function ctaHandler(e) {
				switch (e.type)
				{
				case "mouseover" :
					cta.style.backgroundColor = "#000000";
					break;
				case "mouseout" :
					cta.style.backgroundColor = pColor;
					break;
				case "mousedown" :
					cta.style.backgroundColor = "#666666";
					break;
				case "mouseup" :
					cta.style.backgroundColor = pColor;
					break;
				case "click" :
					Enabler.exitOverride("cta_clicked", dynamicContent.CET_2018_Dynamic_Feed[0].Exit_URL.Url);
					break;
				}

			}
	
